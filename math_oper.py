def sum(a,b):
    result = a + b
    return result

def difference(a,b):
    result = a-b
    return result

def product(a,b):
    result = a*b
    return result

def division(a,b):
    result = a/b
    return result

def squared(a):
    result = a ** a
    return result 

def power(a,b):
    result = a ** b 
    return result

def list_sum(N):
    somma=0
    for i in N:
        somma += i        
    return somma

